
import java.util.*;

public class Nestedloops {

    public static void main(String[] args) throws Exception{
        Scanner sc = new Scanner(System.in);

        
        //Star Pattern



        for(int a = 1; a<=4; a++){
            for(int b = 1 ; b<=a ; b++){
                System.out.print("*");
            }
            System.out.println();
        }

        


}
}